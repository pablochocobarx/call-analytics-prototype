def get_client_am(agent_identifier: str) -> tuple[str, str, float]:
    """Returns (client_display_name, am_name, ql_rate_per_lead)"""
    identifier_lower = agent_identifier.lower()

    # Try to match top-level client key
    matched_client = None
    for client_key in PROJECTS_DATA.keys():
        if client_key in identifier_lower:
            matched_client = client_key
            break

    if not matched_client:
        return ("Unknown", "—", 0.0)

    client_projects = PROJECTS_DATA[matched_client]

    # Try to match a sub-project within the client
    matched_project = None
    matched_project_key = None
    for project_key in client_projects.keys():
        project_key_lower = project_key.lower()
        if project_key_lower in identifier_lower:
            matched_project = client_projects[project_key]
            matched_project_key = project_key
            break

    # If no exact sub-project match, use the first project
    if not matched_project:
        first_project_key = next(iter(client_projects.keys()))
        matched_project = client_projects[first_project_key]
        matched_project_key = first_project_key

    # Extract AM name and rate with fallback priority
    am_name = matched_project.get("account_manager_name", "—")
    ql_rate = matched_project.get("qualified_lead_rate")
    if ql_rate is None:
        ql_rate = matched_project.get("enriched_lead_rate")
    if ql_rate is None:
        ql_rate = matched_project.get("raw_lead_rate")
    if ql_rate is None:
        ql_rate = 0.0

    client_display_name = CLIENT_DISPLAY_NAMES.get(matched_client, matched_client)

    return (client_display_name, am_name, float(ql_rate))


def compute_revenue(agent_identifier: str, qualified_count: int) -> int:
    """Returns estimated revenue in INR."""
    _, _, ql_rate = get_client_am(agent_identifier)
    return int(ql_rate * qualified_count)
